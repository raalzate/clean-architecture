package com.techandsolve.usecase.compress;

import org.apache.pdfbox.contentstream.PDFStreamEngine;
import org.apache.pdfbox.contentstream.operator.DrawObject;
import org.apache.pdfbox.contentstream.operator.Operator;
import org.apache.pdfbox.contentstream.operator.state.*;
import org.apache.pdfbox.cos.COSBase;
import org.apache.pdfbox.cos.COSName;
import org.apache.pdfbox.pdmodel.graphics.PDXObject;
import org.apache.pdfbox.pdmodel.graphics.form.PDFormXObject;
import org.apache.pdfbox.pdmodel.graphics.image.PDImageXObject;
import org.apache.pdfbox.util.Matrix;

import java.io.IOException;
import java.util.List;
import java.util.function.BiFunction;

public class GetImageLocationsAndSize extends PDFStreamEngine {

    private BiFunction<Metadata, PDImageXObject, Void> callback;

    public GetImageLocationsAndSize(BiFunction<Metadata, PDImageXObject, Void> callback) {
        addOperator(new Concatenate());
        addOperator(new DrawObject());
        addOperator(new SetGraphicsStateParameters());
        addOperator(new Save());
        addOperator(new Restore());
        addOperator(new SetMatrix());
        this.callback = callback;
    }

    @Override
    protected void processOperator(Operator operator, List<COSBase> operands) throws IOException {
        String operation = operator.getName();
        if ("Do".equals(operation)) {
            COSName objectName = (COSName) operands.get(0);
            PDXObject xobject = getResources().getXObject(objectName);

            if (xobject instanceof PDImageXObject) {

                PDImageXObject image = (PDImageXObject) xobject;

                int imageWidth = image.getWidth();
                int imageHeight = image.getHeight();
                Metadata metadata = new Metadata();

                Matrix ctmNew = getGraphicsState().getCurrentTransformationMatrix();
                float imageXScale = ctmNew.getScalingFactorX();
                float imageYScale = ctmNew.getScalingFactorY();

                metadata.setDisplayX(imageXScale);
                metadata.setDisplayY(imageYScale);


                metadata.setWidth(imageWidth);
                metadata.setHeight(imageHeight);
                metadata.setObjectName(objectName);

                callback.apply(metadata, image);


            } else if (xobject instanceof PDFormXObject) {
                PDFormXObject form = (PDFormXObject) xobject;
                showForm(form);
            }
        } else {
            super.processOperator(operator, operands);
        }
    }


}

